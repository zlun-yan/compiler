public class Pair {
    private String VN;
    private String VT;

    public Pair(String VN, String VT) {
        this.VN = VN;
        this.VT = VT;
    }

    public String getVN() {
        return VN;
    }

    public void setVN(String VN) {
        this.VN = VN;
    }

    public String getVT() {
        return VT;
    }

    public void setVT(String VT) {
        this.VT = VT;
    }
}
